	/**
	 * Will handle all the Action Listener of the button
	 */
	@Override()
	public void actionPerformed(ActionEvent e)
	{
		if(e.getSource() == btnCreateNewCanvas)
		{
			this.dispose();
			try
			{
				this.client.createCanvasRequest();
			}
			catch(Exception ex)
			{
				//IGNORE
			}
		}
		else if(e.getSource() == btnJoinExistingCanvas)
		{
			this.dispose();
			SelectCanvasServer toJoinCanvas = new SelectCanvasServer(client,this);
			toJoinCanvas.setVisible(true);
		}
		else if(e.getSource() == btnLogout)
		{
			//Should disconnect from main server
			try
			{
				client.logout();
			}
			catch(Exception ex)
			{
				//ignore
			}


			this.dispose();
			frame.setVisible(true);
		}
		
	}

